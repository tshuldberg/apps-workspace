/** Draw a single 8x8 pixel character */
export function drawChar(
  ctx: CanvasRenderingContext2D,
  char: string,
  x: number,
  y: number,
  color: string = PALETTE.BLACK,
): void {
  const glyph = FONT[char];
  if (!glyph) return;
  ctx.fillStyle = color;
  for (let row = 0; row < 8; row++) {
    const bits = glyph[row]!;
    for (let col = 0; col < 8; col++) {
      if (bits & (0x80 >> col)) {
        ctx.fillRect(x + col, y + row, 1, 1);
      }
    }
  }
}

/** Draw a string of text, 8 pixels per character */
export function drawText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string = PALETTE.BLACK,
): void {
  for (let i = 0; i < text.length; i++) {
    drawChar(ctx, text[i]!, x + i * 8, y, color);
  }
}

/** Return pixel width of a string (8 pixels per character) */
export function measureText(text: string): number {
  return text.length * 8;
}

/** Word-wrap text to fit within maxWidth pixels */
export function wordWrap(text: string, maxWidth: number): string[] {
  const maxChars = Math.floor(maxWidth / 8);
  const words = text.split(' ');
  const lines: string[] = [];
  let current = '';
  for (const word of words) {
    if (current.length === 0) {
      current = word;
    } else if (current.length + 1 + word.length <= maxChars) {
      current += ' ' + word;
    } else {
      lines.push(current);
      current = word;
    }
  }
  if (current.length > 0) lines.push(current);
  return lines;
}

/** Draw text centered horizontally at the given y position */
export function drawTextCentered(
  ctx: CanvasRenderingContext2D,
  text: string,
  y: number,
  color: string = PALETTE.BLACK,
  screenWidth: number = 160,
): void {
  const w = measureText(text);
  drawText(ctx, text, Math.floor((screenWidth - w) / 2), y, color);
}

/** Draw large text (2x scale) for titles */
export function drawTextLarge(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string = PALETTE.BLACK,
): void {
  for (let i = 0; i < text.length; i++) {
    const char = text[i]!;
    const glyph = FONT[char];
    if (!glyph) continue;
    ctx.fillStyle = color;
    for (let row = 0; row < 8; row++) {
      const bits = glyph[row]!;
      for (let col = 0; col < 8; col++) {
        if (bits & (0x80 >> col)) {
          ctx.fillRect(x + i * 16 + col * 2, y + row * 2, 2, 2);
        }
      }
    }
  }
}
